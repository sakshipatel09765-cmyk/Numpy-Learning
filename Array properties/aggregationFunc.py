# np.sum(array) - add all the elements of the array
#np.mean(array) - mean of the array
#np.max(array) - maximum value in the array
#np.min(array) - minimum value in the array
#np.std(array) - standard deviation of the array
#np.var(array) - variance of the array 

import numpy as np
arr = np.array([1,2,3,4])
print(np.sum(arr)) #sum of all elements
print(np.mean(arr)) #mean of the array  
print(np.max(arr)) #maximum value in the array
print(np.min(arr)) #minimum value in the array 
print(np.std(arr)) #standard deviation of the array
print(np.var(arr)) #variance of the array 